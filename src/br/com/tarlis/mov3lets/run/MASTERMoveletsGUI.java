    private void btnQueueActionPerformed(java.awt.event.ActionEvent evt) {                                         
        queue.add(prepareRun());
    }      
    
    /**
     * Prepare run.
     *
     * @return the list
     */
    private List<String> prepareRun() {
    	List<String> params = new ArrayList<String>();

		params.add("-curpath"); 				params.add("\'" + txFileds.getText() + "\'");
		params.add("-respath"); 				params.add("\'" + txFileRes.getText() + "\'");
		params.add("-descfile"); 				params.add("\'" + txDescriptor.getText() + "\'");
		params.add("-nt"); 						params.add(txThreads.getText());
		params.add("-ms"); 						params.add(txms.getText());
		params.add("-Ms"); 						params.add(txMs.getText()); // unlimited maxSize
		params.add("-q"); 						params.add(txQ.getSelectedItem().toString().split(" ")[0]); // LSP | PROP
		params.add("-mnf"); 					params.add(txEd.getText().toString());
		params.add("-samples"); 				params.add(txSamples.getText());		
		params.add("-sampleSize"); 				params.add(txSampleSize.getText());			
		params.add("-medium"); 					params.add(txMedium.getSelectedItem().toString()); // Other values minmax); 		params.add( sd); 		params.add( interquartil
		params.add("-output"); 					params.add(txOut.getSelectedItem().toString()); // Other values numeric discretized
		params.add("-interning"); 				params.add(ckInterning.isSelected() + "");
		params.add("-verbose"); 					params.add("true");
		
		if (rbV1.isSelected()) {
			params.add("-version"); 		params.add("1.0");
		} else if (rbV2.isSelected()) {
			params.add("-version"); 		params.add("2.0");
    	} else if (rbSuper.isSelected()) {
			params.add("-version"); 		params.add("super");
		} else if (rbHiper.isSelected()) {
			params.add("-version"); 		params.add("hiper");
		}

		println("Added to Run:");
		printCommand(params);
				
		return params;
    }

