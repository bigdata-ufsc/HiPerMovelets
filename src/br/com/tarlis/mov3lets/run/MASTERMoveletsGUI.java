    /**
     * Btn run action performed.
     *
     * @param evt the evt
     */
    private void btnRunActionPerformed(java.awt.event.ActionEvent evt) {                                       
        if (queue.isEmpty()) {
        	queue.add(prepareRun());
        }
        
        for (List<String> args : queue) {
        	println("");
    		println("Running:");
        	printCommand(args);
    		println("----------------------------------------------------------------");
        	Mov3letsRun.main((String[]) args.toArray(new String[0])); 
    		println("----------------------------------------------------------------");
		}
        
        queue.clear();
    }                                      

